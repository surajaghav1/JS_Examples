// String methods in js 
let name="Ram";
console.log("original name : ",name)

// concat the string 
let surname="sharma"
console.log(name.concat(surname));

// trim() =>> removes the unwanted space from string
let trimstr="    hi guys"
console.log("str without trim() : ",trimstr)
console.log("string with trim function :",trimstr.trim())

//string replace we can replace the specific string 
let sentence="hi my name is suraj ";
console.log(sentence)
console.log(sentence.replace("suraj","ram"))


